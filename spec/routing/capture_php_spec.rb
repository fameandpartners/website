require 'spec_helper'

describe 'Capture `.php` requests', type: :request do

  it 'receives not acceptable' do
    get '/abc.php'
    expect(response).to have_http_status :not_acceptable
    post '/abc.php'
    expect(response).to have_http_status :not_acceptable
  end

  it 'receives not found' do
    get '/abc'
    expect(response).to have_http_status :not_found
    post '/abc'
    expect(response).to have_http_status :not_found
    get '/abc.js'
    expect(response).to have_http_status :not_found
  end
end
